    public void RegistrarUsuarios(){
        String u = txtusuario.getText();
        String p = password.getText();
        String SQL = "INSERT INTO usuarios (n_usuario, u_password) VALUES (?,?)";
        try {
            PreparedStatement pst = con.prepareStatement(SQL);
        
            pst.setString(1,u);
            pst.setString(2, p);
            pst.execute();
        } catch (Exception e) {
        }
    
    }

